function sum(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function mul(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}

function stepen(a, b) {
    return a ** b;
}

function chto_to(a, b) {
    return a + b - b;
}

function kto_to(a, b) {
    return a - b - b;
}

module.exports = sum;